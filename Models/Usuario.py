class Usuario():
    def __init__(self,nome,email,senha,nascimento,profissao,genero,cidade,estado,pais):
        self.nome = nome
        self.email = email
        self.senha = senha
        self.nascimento = nascimento
        self.profissao = profissao
        self.genero = genero
        self.cidade = cidade
        self.estado = estado
